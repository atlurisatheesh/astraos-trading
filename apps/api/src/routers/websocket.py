# Channel 5: /ws/alerts — Real-Time Price Alerts
# ═══════════════════════════════════════════════════════════════

@router.websocket("/ws/alerts")
async def ws_alerts(websocket: WebSocket):
    """Stream real-time price alerts from the live scanner.

    Pushes spike, crash, breakout, and volume surge alerts.
    Checks every 1 second.
    """
    channel = "alerts"
    await manager.connect(websocket, channel)
    last_alert_count = 0

    try:
        while True:
            from ..scheduler.live_scanner import get_alerts, get_scanner_stats

            alerts = get_alerts(limit=20)
            stats = get_scanner_stats()
            current_count = stats.get("alerts_count", 0)

            if current_count > last_alert_count:
                new_alerts = alerts[:current_count - last_alert_count]
                await websocket.send_json({
                    "type": "alerts",
                    "timestamp": datetime.now(timezone.utc).isoformat(),
                    "alerts": new_alerts,
                    "scanner_stats": stats,
                })
                last_alert_count = current_count

            await asyncio.sleep(1)

    except WebSocketDisconnect:
        manager.disconnect(websocket, channel)
    except Exception as e:
        logger.error("Alerts WS error", error=str(e))
        manager.disconnect(websocket, channel)


